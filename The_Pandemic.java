import java.util.*;
public class main{
    public static void main(String[] args){
        Scanner read=new Scanner(System.in);
        int N=read.nextInt();
        if(N==1){
            System.out.printf("0");
        }
        else if(N==2){
            System.out.printf("1");
        }
        else{
            System.out.printf("2");
        }
        
    }
}